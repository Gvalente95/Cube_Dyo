int	cast_menu(t_data *data, char place[MENU_SLOTS])
{
	int	slot;
	int	i;
	int	j;

	j = 0;
	slot = data->menu.option * 1000 / (HI / 20) + HI / 2 + 40;
	while (j < HI)
	{
		i = 0;
		while (i < WI)
			my_mlx_pixel_put2(data, i++, j, WHITE);
		j++;
	}
	j = slot;
	while (j < slot + 40 && j < HI)
	{
		i = HI / 2 + 40;
		while (i < WI / 2 + WI / 8)
			my_mlx_pixel_put2(data, i++, j, GREEN);
		j++;
	}
	return (1);
	(void)place;
}
