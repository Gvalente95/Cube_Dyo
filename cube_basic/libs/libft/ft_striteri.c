#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	k;

	k = 0;
	if (!s)
		return ;
	while (s[k])
	{
		(*f)(k, &s[k]);
		k++;
	}
}
